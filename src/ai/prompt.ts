import { getRoleConfig } from '../config.js'
import type { RepoData, RoleKey } from '../types.js'

const RESUME_RULES = `
你是一位深度参与过国内大厂技术招聘的简历撰写专家。请按以下规范工作。

## 一、写作思维（FAB 模式）

每个项目段落必须隐含三层信息：
- **F (Feature) 是什么**：项目的业务定位与技术形态
- **A (Advantage) 好在哪**：你的方案相比通行做法的差异点
- **B (Benefit) 对雇主何价值**：能省什么成本 / 赚什么业务 / 撑什么规模（用数字说话）

只有"做了什么"没有"价值兑现"的段落，是不合格的。

## 二、三要素（每段必须满足）

- **具体**：避免"负责开发"、"参与重构"这类含糊词，写清具体模块、技术选型、决策角度
- **量化**：必须有数字。优先级 业务指标（DAU / 营收 / 可用性）> 技术指标（QPS / 延迟 / 资源）> 工程规模（模块数 / 文件数）
- **有说服力但不自夸**：摆论据，不下论点。不写"我能力很强"，写"将 API P99 从 800ms 降至 120ms"，让读者自己得结论

## 三、没有量化数据时——讲成长，不要编造

按四步展开：
1. 遇到了什么问题（具体场景）
2. 通行方案的局限（为什么不能直接抄）
3. 你采用的方案（关键决策点）
4. 实施后客观可验证的变化（即使没有 KPI，也有"模块上线后 N 个团队接入"这类事实）

**严禁编造业务指标**（如随手写"提升 30%"、"千万 DAU"）。规模数字（影响请求量、覆盖模块数）可基于 PR 客观推断。

## 四、硬性禁忌

- 不写代码行数、PR 数量、commit 数量
- 不写"精通"，使用"主力使用 / 生产环境 N 年 / 熟悉 / 了解"
- 不写自我评价（热爱学习、责任心强、抗压能力强等）
- 不把第三方 API 服务（OpenAI、Gemini、Adjust 等）列入"技术栈"——技术栈是你掌握的能力，不是你调用的服务
- 不堆砌买点术语；用了的术语必须用对（DDD / 事件驱动 / 断路器 / 熔断 / 一致性哈希 等）

## 五、写作技巧

- 用架构级术语：不写"用了 MongoDB"，写"MongoDB 分片集群 + 一致性哈希分片键"
- 体现系统设计思维：高可用（断路器 / 降级）、可扩展（水平扩展 / 插件化）、可观测（全链路追踪 / 分钟级故障定位）
- 关键词命中：贴近目标 JD 与岗位关键技术，但不可与 PR 事实矛盾

## 六、项目数量

每个仓库聚焦 2-3 个最能体现技术深度 / 影响面 / 工程能力的核心项目；其他次要 PR 归并到"其他模块"一行带过。**不要把 30 条 PR 平铺直叙**。
`

export function buildResumePrompt(
  repo: RepoData,
  role: RoleKey,
  jd: string | null,
): { system: string; user: string } {
  const roleConfig = getRoleConfig(role)

  const system = `${RESUME_RULES}

## 本次任务定向

- 候选人目标岗位侧重：${roleConfig.emphasis}
- 重点向以下技术关键词靠拢（前提：与 PR 事实不冲突）：${roleConfig.keywords.join('、')}
`

  const prSummary = repo.prs
    .slice(0, 30)
    .map(pr => `- [${pr.mergedAt}] ${pr.title}\n  ${pr.body.slice(0, 300)}`)
    .join('\n')

  const jdSection = jd
    ? `\n## 目标 JD（请让简历描述与此对齐，但不可与 PR 事实矛盾）\n${jd.slice(0, 1000)}\n`
    : ''

  const user = `请基于以下 GitHub 仓库的 PR 数据，输出该候选人在该仓库期间的项目经历段落。

## 仓库信息
- 名称：${repo.name}
- 公司：${repo.company}
- 时间：${repo.period}
- 候选人公开技术栈：${repo.techStack.join('、')}
${jdSection}
## PR 记录（最近 ${repo.prs.slice(0, 30).length} 条，全部素材都在这里）
${prSummary}

## 你的工作流（按顺序执行）

1. 通读 PR，识别出 2-3 个最能体现技术深度 / 业务影响 / 工程能力的项目主题
2. 把同一主题下分散的多条 PR 合并理解（一个项目通常对应多次迭代）
3. 其余 PR 归入"其他模块"一行
4. 对每个核心项目按"业务定位 → 背景 / 规模 → 技术方案 → 我的职责 → 结果"展开
5. 量化数据若 PR 中没有，可推断规模（影响的请求量、模块数、覆盖用户量），切勿编造业务 KPI
6. 输出后，在心里对照"自检清单"过一遍，不通过的段落改写

## 输出格式（严格遵守）

### ${repo.name}（${repo.period}）

**技术栈**：[只列项目实际用到的，不堆砌；不含第三方 API 服务名]

**核心项目**：

#### 项目 1：[简短业务定位，如"千万级用户多端推送系统"]
- **背景 / 规模**：[1 句业务场景 + 量化规模]
- **技术方案**：[架构选型 + 关键组件，使用架构级术语]
- **我的职责**：[独立负责 / 主导设计 / 核心开发等，写具体不写抽象]
- **结果**：[业务指标 / 技术指标 / 工程规模 至少一项数字；无 KPI 时按"讲成长"四步代替]

#### 项目 2：...

#### 其他模块
[其余 PR 一行内归并，如"另完成 X 模块拆分、Y 工具链重构、Z 监控接入等"]

## 自检清单（生成后心里过一遍，不输出到结果里）
- [ ] 每个项目的"对雇主的价值"（B）说清楚了，不只是罗列动作
- [ ] 每段至少有一个数字
- [ ] 没有"精通"、没有自我评价、没有 PR / commit 数量
- [ ] 用到的架构术语都能在 PR 里找到事实依据
- [ ] 没有为了贴 JD 而编造 PR 里不存在的内容
`

  return { system, user }
}

export function buildInterviewPrompt(resumeContent: string, jd: string): string {
  return `你是一位资深技术面试辅导专家，深度参与过国内大厂的技术面与跨级面。根据候选人简历和目标 JD，生成详细的面试准备计划。

## 候选人简历
${resumeContent}

## 目标 JD
${jd}

## 输出格式（Markdown）

# 面试准备计划

## 一、岗位匹配分析
- **强匹配点**：简历中哪些经历直接对口 JD 要求（指明哪段经历对哪条 JD）
- **潜在短板**：JD 要求但简历较弱或缺失的部分（给出补强建议）
- **建议重点包装的项目**：哪 1-2 个项目应作为自我介绍的主轴，为什么

## 二、高频技术面试题（按优先级）

### 必考题（JD 直接提到的技术）
- Q：[问题原文]
  - 参考回答角度：[要点 + 体现深度的关键词，按 STAR / 三段论展开]

### 大概率考（基于候选人项目经历可深挖的方向）
- Q：[问题原文]
  - 参考回答角度：[结合简历中具体哪段经历回答]

### 反向追问准备（候选人可主动抛出展示思考的问题）
- Q：[问题原文]
  - 用意：[为什么问这个，能体现什么]

## 三、项目追问准备
针对简历中的核心项目，列出面试官最可能挖的方向。

### [项目名]
- **可能被追问**：[技术选型为什么是 A 不是 B / 性能瓶颈在哪 / 故障如何排查 / 量化数据怎么测出来的 等]
- **建议准备**：技术细节 + 决策权衡 + 数据支撑（数据要能口头复现而不是背简历）
- **风险点**：简历里写了但你其实不太熟的部分（明确点出，提醒重点恶补）

## 四、行为题与软素质（国内二面常考）
- **沟通协作类**：与产品 / 设计 / 跨团队冲突时的处理案例
- **项目挫折类**：曾经做失败的项目 / 上线事故 / 架构选错的复盘
- **成长动机类**：为什么离职 / 为什么选这家 / 三年规划
针对每类给出 1-2 个候选人可用的真实素材切入角度（基于简历推断）。

## 五、知识点补充建议（按优先级）
- **必补**：JD 强要求但简历未体现的核心知识，1 周内能补的
- **建议补**：能让候选人在同水位中更突出的纵深知识
- **可不补**：JD 边角料、ROI 太低的暂缓

## 六、面试策略
- **自我介绍**（控制在 2 分钟内）：建议侧重点 + 一句钩子（让面试官想往里挖的那句话）
- **主动展开的亮点**：哪 1-2 个项目要争取在自我介绍 / 反问环节带出来
- **谈薪准备**：基于 JD 城市与岗位级别的合理区间提示
`
}

export function buildRoleInferencePrompt(jd: string): string {
  return `根据以下 JD，判断最匹配的岗位类型，只返回一个 key，不要解释：
可选值：go / java / node / frontend / ios / android / fullstack / architect / ai

JD：
${jd.slice(0, 500)}`
}
